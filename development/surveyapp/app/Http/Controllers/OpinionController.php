    {
        $now = Carbon::today();
        $today = $now->toDateString();
        
        $results = DB::select( DB::raw("SELECT `option` FROM tbl_opinions WHERE created_at LIKE '$today%'"));

        return \View::make("opinions.index")->with(array('opinionResults'=>$results));
